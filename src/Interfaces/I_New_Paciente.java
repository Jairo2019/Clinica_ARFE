    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
               try{
            con=Conexion.ConnectDB();
            if (txtPatientID.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Ingrese Numero de Identidad","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtPatientName.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Ingrese Nombre","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtPatientlastname.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Ingrese Apellido","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }if (txtage.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Ingrese Edad","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (txtphone.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Ingrese numero de Telefono","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Statement stmt;
            stmt= con.createStatement();
            String sql1="Select PacienteIdentidad from paciente where PacienteIdentidad= '" + txtPatientID.getText() + "'";
            ResultSet rs = stmt.executeQuery(sql1);
            if(rs.next()){
                JOptionPane.showMessageDialog( this, "Numero de Identidad ya existe","Error", JOptionPane.ERROR_MESSAGE);
                txtPatientID.setText("");
                txtPatientID.requestFocus();
                return;
            }
            if (txtPatientID.getText().length() < limite){
                JOptionPane.showMessageDialog( this, "Al Numero de Identidad le Faltan D√≠gitos ","Error", JOptionPane.ERROR_MESSAGE);
                txtPatientID.requestFocus();
                return;
            }
            String sql= "insert into paciente(PacienteIdentidad, PacienteFecha, PacienteNombres, PacienteApellidos, PacienteEdad,PacienteSexo, PacienteTelefono, PacienteCorreo) values ('"+txtPatientID.getText()+"','" +fechaactual()+"','" +txtPatientName.getText()+"','" + txtPatientlastname.getText()+"','" +txtage.getText()+ "','" +cmbGender.getSelectedItem()+"','" +txtphone.getText()+"','" +txtEmail.getText()+ "')";
            pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Registrado con √©xito","Registro de Pacientes",JOptionPane.INFORMATION_MESSAGE);
            obtener_Data();
            this.dispose();
            limpiar();
            
        }catch( SQLException e){
          //  JOptionPane.showMessageDialog(this,ex);
        }
    }//GEN-LAST:event_btnguardarActionPerformed

