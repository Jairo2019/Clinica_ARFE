    //Guardasr categorias a la DB
    void save_category(){
                       try{
            con=Conexion.ConnectDB();
            if (txtPatientID.getText().equals("")) {
                JOptionPane.showMessageDialog( this, "Ingrese Nombre de la Categoria","Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            
//cadena de conexion
            String sql= "insert into tipoexamen(TipoExamenNombre,"
                    + " TipoExamenDescripcion)"
                    + " values ('"+txtPatientID.getText()+"','" 
                    +txtRemarks.getText()+"')";
            pst=con.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(this,"Registrado con éxito","Registro de Pacientes",JOptionPane.INFORMATION_MESSAGE);
           get();
            limpiar();
            
        }catch( SQLException e){
           JOptionPane.showMessageDialog(this,e);
        }
    }
//limpiar las cajas de texto
    void limpiar(){
        txtPatientID.setText("");
        txtRemarks.setText("");
    }
    void get(){//obtener las categorias de la db
    String sql="select idTipoExamen as 'codigo',"
            + "TipoExamenNombre as 'Nombre del Examen', "
            + "TipoExamenDescripcion as 'Descripción'"
            + "from tipoexamen ";
        try{
         pst=con.prepareStatement(sql);
          rs= pst.executeQuery();
          TblDoc.setModel(DbUtils.resultSetToTableModel(rs));
         }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
          
}
}
